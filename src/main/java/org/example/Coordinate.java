package org.example;

// class for coordinate position
public class Coordinate {
    public int x;
    public int y;
    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public Coordinate() {

    }

    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }


}